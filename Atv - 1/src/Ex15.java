public class Ex15 {
    public static void main(String[] args) {
        double num1 = 14;
        for (int i = 10; i >= 1; i--) {
                System.out.println(num1 + " * " + i + " = " + (num1 * i));
            }
        for (int i = 10; i >= 1; i--) {
            System.out.println(num1 + " / " + i + " = " + (num1 / i));
        }
        for (int i = 10; i >= 1; i--) {
            System.out.println(num1 + " + " + i + " = " + (num1 + i));
        }
        for (int i = 10; i >= 1; i--) {
            System.out.println(num1 + " - " + i + " = " + (num1 - i));
        }
        }
    }
